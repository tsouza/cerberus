func renderHeadlines(b *strings.Builder, in docInput) {
	b.WriteString("## Headline before/after wins\n\n")
	b.WriteString("Each win pairs the **naive** pre-optimization SQL shape against the ")
	b.WriteString("**optimized** shape cerberus actually emits, both run live on chDB over a ")
	b.WriteString("seeded dataset. The `win` column is the headline ratio: a deterministic ")
	b.WriteString("structural ratio where one exists (granules read, rows scanned, peak ")
	b.WriteString("intermediate cardinality), else the indicative wall-time speedup. The ")
	b.WriteString("per-win detail below carries both axes.\n\n")

	var rows [][]string
	for _, w := range in.wins {
		ratio, basis := headlineRatio(w)
		rows = append(rows, []string{w.Name, fmt.Sprintf("%.1f×", ratio), basis})
	}
	writeTable(b, []align{alignLeft, alignRight, alignLeft},
		[]string{"optimization", "win", "basis"}, rows)
	b.WriteString("\n")

	// Per-win detail as a list (long descriptions go here, not in cells).
	for _, w := range in.wins {
		fmt.Fprintf(b, "### %s\n\n", w.Name)
		fmt.Fprintf(b, "- **Scenario:** %s\n", w.Param)
		fmt.Fprintf(b, "- **Naive:** %s\n", w.Naive)
		fmt.Fprintf(b, "- **Optimized:** %s\n", w.Optimized)
		if w.StructRatio > 0 {
			fmt.Fprintf(b, "- **%s (deterministic):** %s → %s (**%.1f× fewer**)\n",
				w.StructLabel, fmtInt(w.NaiveStruct), fmtInt(w.OptStruct), w.StructRatio)
		}
		if w.WallNote != "" {
			fmt.Fprintf(b, "- **Wall (indicative):** %s → %s — %s\n",
				roundDur(w.NaiveWall), roundDur(w.OptWall), w.WallNote)
		} else {
			fmt.Fprintf(b, "- **Wall (indicative):** %s → %s (**%.1f× faster**)\n",
				roundDur(w.NaiveWall), roundDur(w.OptWall), w.Speedup)
		}
		b.WriteString("\n")
	}
}

// headlineRatio picks the headline ratio + a short basis label for the
// summary table, honouring each win's Primary axis.
func headlineRatio(w headlineWin) (float64, string) {
	if w.Primary == "structural" && w.StructRatio > 0 {
		return w.StructRatio, w.StructLabel + " (deterministic)"
	}
	return w.Speedup, "wall time (indicative)"
}

// renderSharded emits the SHARDED-SOLVER dimension: the OOM-class range query
// route A cannot serve under the 1 GiB per-query cap, that route B (the sharded
// solver) clears. The table contrasts route A (single statement, whole anchor
// grid) against route B (K shards, each its anchor sub-grid) on the MEASURED
// expanded (sample, anchor) pair count — the deterministic driver of
// per-statement memory — plus the MODELED GiB derived from it against the cap.
func renderSharded(b *strings.Builder, in docInput) {
	sh := in.sharded
	b.WriteString("## Sharded solver — the OOM-class win\n\n")

	fmt.Fprintf(b, "The sharded-pushdown solver (`internal/solver`, ")
	b.WriteString("[`docs/query-solver-design.md`](query-solver-design.md)) recognizes the narrow ")
	b.WriteString("class of range queries whose **single-statement** execution is memory-unbounded ")
	b.WriteString("on ClickHouse — a high anchor fan-out `F = Range/Step` matrix shape — and ")
	b.WriteString("re-anchors the *same* optimized plan onto **K disjoint anchor-grid shards**, ")
	b.WriteString("each running the identical compat-gated SQL restricted to its anchor sub-grid. ")
	b.WriteString("No new evaluator, no new SQL template.\n\n")

	fmt.Fprintf(b, "**Fixture (OOM class).** `%s` @ %s over %s — F = Range/Step = **%d**, "+
		"N = OuterRange/Step + 1 = **%d**. Seeded: **%s** series, **%s** scanned samples "+
		"(one sample / %s across the input window, generated server-side via `numbers(N)`).\n\n",
		sh.Query, fmtGridDur(sh.Step), fmtGridDur(sh.OuterRange), sh.F, sh.N,
		fmtInt(sh.Series), fmtInt(sh.ScanRows), fmtGridDur(sh.Step))

	// The contrast table.
	routeAClears := sh.RouteAModeledBytes <= sh.CapBytes
	maxShardClears := sh.MaxShardModeledBytes <= sh.CapBytes
	rows := [][]string{
		{
			"A — single statement",
			"1",
			fmtInt(sh.RouteAPairs),
			fmtBytes(sh.RouteAModeledBytes),
			verdict(routeAClears),
		},
		{
			fmt.Sprintf("B — sharded (K=%d)", sh.K),
			fmt.Sprintf("%d", sh.K),
			fmtInt(sh.MaxShardPairs) + " (max)",
			fmtBytes(sh.MaxShardModeledBytes),
			verdict(maxShardClears),
		},
	}
	writeTable(b, []align{alignLeft, alignRight, alignRight, alignRight, alignLeft},
		[]string{"route", "statements", "(sample,anchor) pairs", "modeled peak", "vs " + fmtBytes(sh.CapBytes) + " cap"},
		rows)
	b.WriteString("\n")

	routeACmp := "**exceeds** it — the OOM 422 the k3d run that motivated the cap actually hit"
	if routeAClears {
		routeACmp = "sits under it"
	}
	fmt.Fprintf(b, "- **Cap.** The prod per-query memory cap is **%s** "+
		"(`CERBERUS_CH_QUERY_MAX_MEMORY`, the default `chclient` stamps on every data-plane "+
		"query). Route A's modeled peak (**%s**) %s. Every route-B shard's modeled peak (**%s**) "+
		"sits **under** the cap (**%.1f× headroom**).\n",
		fmtBytes(sh.CapBytes), fmtBytes(sh.RouteAModeledBytes), routeACmp, fmtBytes(sh.MaxShardModeledBytes),
		float64(sh.CapBytes)/float64(sh.MaxShardModeledBytes))
	fmt.Fprintf(b, "- **Pairs are MEASURED.** The (sample, anchor) pair count is the deterministic "+
		"driver of per-statement memory: the matrix `arrayJoin` fan-out materializes one row "+
		"per (sample, covered anchor) before the `GROUP BY` collapse. It is counted **live on "+
		"chDB** over the seed — route A over the whole %d-anchor grid, each shard over only its "+
		"sub-grid. The K shards partition route A's pairs exactly: Σ per-shard = %s = route-A "+
		"%s (no double-count), and the worst shard carries %s — a **%.1f×** reduction, tracking "+
		"1/K.\n",
		sh.N, fmtInt(sh.SumShardPairs), fmtInt(sh.RouteAPairs), fmtInt(sh.MaxShardPairs),
		ratioI(sh.RouteAPairs, sh.MaxShardPairs))
	fmt.Fprintf(b, "- **GiB are MODELED, not measured.** chDB does **not** enforce a memory cap "+
		"(it coerces leniently and never OOMs), so this dimension does **not** claim a measured "+
		"OOM. The modeled-peak columns scale the **measured** pair counts by a single published "+
		"calibration constant — the worked spike's 2.12 GiB at ≈100.4M pairs "+
		"(≈%.1f B/pair, k3d run 27277793810). The honest claim: the **measured** pair-count "+
		"reduction is real and deterministic; the GiB framing maps it onto the prod cap the "+
		"design exists to respect.\n", bytesPerPair())

	// Per-shard pair breakdown (a list, so MD060 stays clean for long rows).
	if len(sh.ShardPairs) > 0 {
		b.WriteString("\nPer-shard measured pairs (oldest-first): ")
		parts := make([]string, len(sh.ShardPairs))
		for i, p := range sh.ShardPairs {
			parts[i] = fmt.Sprintf("shard %d = %s", i, fmtInt(p))
		}
		b.WriteString(strings.Join(parts, ", ") + ".\n\n")
	}
}

// verdict renders the cap-fit verdict for the sharded table.
func verdict(clears bool) string {
	if clears {
		return "clears"
	}
	return "**OOM** (exceeds cap)"
}

func renderScaling(b *strings.Builder, in docInput) {
	b.WriteString("## Scaling curves\n\n")
	b.WriteString("Wall-time and `fan_factor` (peak intermediate cardinality ÷ scan rows) vs ")
	b.WriteString("the real fan-out multiplier for each registered construct ")
	b.WriteString("(`test/perf/scaling/`). A bounded shape keeps `fan_factor` ~flat as the ")
	b.WriteString("parameter grows — the cardinality stays a small constant multiple of the ")
	b.WriteString("scan, never `rows × parameter`.\n\n")

	for _, c := range in.curves {
		fmt.Fprintf(b, "### %s — sweep %s\n\n", c.Name, c.Param)
		fmt.Fprintf(b, "Scan rows held fixed at %s.\n\n", fmtInt(c.ScanRows))
		var rows [][]string
		for _, p := range c.Points {
			rows = append(rows, []string{
				fmt.Sprintf("%d", p.Param), roundDur(p.Wall),
				fmtInt(p.PeakRows), fmt.Sprintf("%.2f", p.FanRatio),
			})
		}
		writeTable(b, []align{alignRight, alignRight, alignRight, alignRight},
			[]string{"param", "wall", "peak rows", "fan_factor"}, rows)
		fmt.Fprintf(b, "\nParameter grew **%.1f×** across the sweep; wall grew **%.1f×** ",
			c.ParamGrowth, c.WallGrowth)
		if c.WallGrowth < c.ParamGrowth {
			b.WriteString("(sub-linear). ")
		} else {
			b.WriteString("(tracks the parameter — see the residual finding below). ")
		}
		fmt.Fprintf(b, "`fan_factor` stayed bounded (%.2f → %.2f).\n\n",
			c.Points[0].FanRatio, c.Points[len(c.Points)-1].FanRatio)
	}

	b.WriteString("> Note: the `setop_chain` wall is the documented residual super-linearity ")
	b.WriteString("(each `or` level re-scans the accumulated relation; the cardinality axis is ")
	b.WriteString("flat). It is quarantined as a tracked finding in the scaling harness ")
	b.WriteString("(N-ary single-pass flatten is the fix); the harness still hard-gates the ")
	b.WriteString("cardinality axis.\n\n")
}

