// align enumerates a markdown table column's alignment for the separator
// row and cell padding.
type align int

const (
	alignLeft align = iota
	alignRight
)

// writeTable emits a markdownlint-MD060-compliant aligned table: every
// pipe lines up because each cell is padded to its column's max width.
// aligns gives the per-column alignment (left/right); header + rows must
// all have len(aligns) cells.
func writeTable(b *strings.Builder, aligns []align, header []string, rows [][]string) {
	widths := make([]int, len(aligns))
	for i, h := range header {
		widths[i] = utf8.RuneCountInString(h)
	}
	for _, r := range rows {
		for i, c := range r {
			if w := utf8.RuneCountInString(c); w > widths[i] {
				widths[i] = w
			}
		}
	}
	// The separator cell is `---`/`---:`; ensure the column is wide enough
	// for it (min 3, +1 for the right-align colon).
	for i, a := range aligns {
		min := 3
		if a == alignRight {
			min = 4
		}
		if widths[i] < min {
			widths[i] = min
		}
	}

	writeRow := func(cells []string) {
		b.WriteString("|")
		for i, c := range cells {
			pad := widths[i] - utf8.RuneCountInString(c)
			if aligns[i] == alignRight {
				b.WriteString(" " + strings.Repeat(" ", pad) + c + " |")
			} else {
				b.WriteString(" " + c + strings.Repeat(" ", pad) + " |")
			}
		}
		b.WriteString("\n")
	}
	writeRow(header)
	// separator
	b.WriteString("|")
	for i, a := range aligns {
		if a == alignRight {
			b.WriteString(" " + strings.Repeat("-", widths[i]-1) + ": |")
		} else {
			b.WriteString(" " + strings.Repeat("-", widths[i]) + " |")
		}
	}
	b.WriteString("\n")
	for _, r := range rows {
		writeRow(r)
	}
}

// renderDoc assembles the full benchmarks.md. Timings are rounded to
// reduce single-process noise; ratios (the stable signal) carry one
// decimal. Tables are emitted via writeTable so pipes align (MD060).
func renderDoc(in docInput) string {
	var b strings.Builder

	b.WriteString("# Cerberus benchmarks\n\n")
	b.WriteString("Live performance measurements for the cerberus query gateway: optimizer ")
	b.WriteString("before/after wins, the **sharded-solver** memory-unbounded win, scaling ")
	b.WriteString("curves, per-stage micro-benchmarks, and end-to-end query latency on a ")
	b.WriteString("realistic dataset. Every number here is **measured**, not estimated — the ")
	b.WriteString("document is regenerated by ")
	b.WriteString("[`just bench-report`](#methodology--how-to-regenerate).\n\n")

	renderTopline(&b, in)
	renderMethodology(&b, in)
	renderHeadlines(&b, in)
	renderSharded(&b, in)
	renderScaling(&b, in)
	renderMicro(&b, in)
	renderE2E(&b, in)

	b.WriteString("## See also\n\n")
	b.WriteString("- [`docs/performance.md`](performance.md) — the optimizer + perf ")
	b.WriteString("architecture these numbers exercise.\n")
	b.WriteString("- [`docs/test-strategy.md`](test-strategy.md) — the perf-assessment ")
	b.WriteString("test layers (scaling harness, corpus profiler, cardinality ratchet) ")
	b.WriteString("that gate structural regressions in CI.\n")

	return b.String()
}

// renderTopline puts the headline at the very top: a one-line summary row per
// dimension, led by the sharded-solver win (the OOM-class range query route A
// cannot serve under the 1 GiB cap that route B clears). It lets a reader see
// the wins before scrolling into the per-dimension detail.
func renderTopline(b *strings.Builder, in docInput) {
	b.WriteString("## At a glance\n\n")

	sh := in.sharded
	var rows [][]string
	// Lead row: the sharded-solver headline. The route-A verdict is derived,
	// not asserted — if a seed resize ever dropped route A under the cap, the
	// doc would say so rather than lie.
	routeAVerdict := "OOM"
	if sh.RouteAModeledBytes <= sh.CapBytes {
		routeAVerdict = "fits"
	}
	rows = append(rows, []string{
		"sharded solver (OOM class)",
		fmt.Sprintf("%.1f×", ratioI(sh.RouteAPairs, sh.MaxShardPairs)),
		fmt.Sprintf("route A %s modeled (%s vs %s cap) → route B K=%d, %s/shard (clears)",
			fmtBytes(sh.RouteAModeledBytes), routeAVerdict, fmtBytes(sh.CapBytes), sh.K, fmtBytes(sh.MaxShardModeledBytes)),
	})
	// Then the top structural optimizer win.
	for _, w := range in.wins {
		ratio, basis := headlineRatio(w)
		rows = append(rows, []string{w.Name, fmt.Sprintf("%.1f×", ratio), basis})
	}
	writeTable(b, []align{alignLeft, alignRight, alignLeft},
		[]string{"dimension", "win", "what it measures"}, rows)
	b.WriteString("\n")

	overVerb := "**OOMs**, over"
	if sh.RouteAModeledBytes <= sh.CapBytes {
		overVerb = "fits under"
	}
	fmt.Fprintf(b, "The **sharded solver** is the headline: a high-fan-out matrix range query "+
		"(`%s` @ %s over %s, F=%d, N=%d) expands ≈%s (sample, anchor) pairs in a single "+
		"statement — a **modeled %s** that %s the **%s** per-query cap "+
		"(`CERBERUS_CH_QUERY_MAX_MEMORY`). The solver re-anchors the same "+
		"optimized plan onto **K=%d** disjoint anchor-grid shards; the worst shard expands "+
		"only ≈%s pairs (**modeled %s**) — every shard **clears** the cap with headroom. "+
		"See [Sharded solver](#sharded-solver--the-oom-class-win).\n\n",
		sh.Query, fmtGridDur(sh.Step), fmtGridDur(sh.OuterRange), sh.F, sh.N,
		fmtInt(sh.RouteAPairs), fmtBytes(sh.RouteAModeledBytes), overVerb, fmtBytes(sh.CapBytes),
		sh.K, fmtInt(sh.MaxShardPairs), fmtBytes(sh.MaxShardModeledBytes))
}

