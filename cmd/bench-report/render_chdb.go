// --- matrix presentation helpers ----------------------------------------

// matrixRows builds the 3×2 table rows in presentation order.
func matrixRows(m matrixResult) [][]string {
	var rows [][]string
	for _, c := range m.Cells {
		routedCell := "no (single statement)"
		stmts := "1"
		if c.Routed {
			routedCell = fmt.Sprintf("yes (K=%d)", c.K)
			stmts = fmt.Sprintf("%d", c.K)
		}
		rows = append(rows, []string{
			c.Route, tsLabel(c.TSGrid), stmts, roundDur(c.Wall), fmtBytes(c.PeakMem), routedCell,
		})
	}
	return rows
}

// matrixChartData splits the cells into grouped-bar inputs: one group per
// route, two series (native off / on), for both wall (ms) and memory (MiB).
func matrixChartData(m matrixResult) (groups, series []string, wall, mem [][]float64) {
	groups = []string{"single", "sharded", "auto"}
	series = []string{"native off", "native on"}
	for _, route := range groups {
		off := matrixCellOf(m, route, false)
		on := matrixCellOf(m, route, true)
		wall = append(wall, []float64{cellWallMs(off), cellWallMs(on)})
		mem = append(mem, []float64{cellMemMiB(off), cellMemMiB(on)})
	}
	return groups, series, wall, mem
}

func cellWallMs(c *matrixCell) float64 {
	if c == nil {
		return 0
	}
	return float64(c.Wall) / float64(time.Millisecond)
}

func cellMemMiB(c *matrixCell) float64 {
	if c == nil {
		return 0
	}
	return float64(c.PeakMem) / float64(1<<20)
}

// matrixCellOf finds the cell for a (route, tsgrid) pair, or nil.
func matrixCellOf(m matrixResult, route string, tsgrid bool) *matrixCell {
	for i := range m.Cells {
		if m.Cells[i].Route == route && m.Cells[i].TSGrid == tsgrid {
			return &m.Cells[i]
		}
	}
	return nil
}

func tsLabel(on bool) string {
	if on {
		return "on"
	}
	return "off"
}

// e2eShortLabel trims an e2e row name to a compact chart x-label.
func e2eShortLabel(name string) string {
	switch {
	case strings.HasPrefix(name, "range query"):
		return "rate range"
	case strings.HasPrefix(name, "instant"):
		return "instant"
	case strings.HasPrefix(name, "series"):
		return "series"
	case strings.HasPrefix(name, "TraceQL"):
		return "TraceQL"
	case strings.HasPrefix(name, "LogQL"):
		return "LogQL"
	}
	return name
}

// --- formatting helpers --------------------------------------------------

// roundDur rounds a duration to a stable, low-noise unit.
func roundDur(d time.Duration) string {
	switch {
	case d < time.Microsecond:
		return d.String()
	case d < time.Millisecond:
		return fmt.Sprintf("%dµs", d.Round(time.Microsecond)/time.Microsecond)
	case d < time.Second:
		return fmt.Sprintf("%.1fms", float64(d)/float64(time.Millisecond))
	default:
		return fmt.Sprintf("%.2fs", d.Seconds())
	}
}

// fmtMs renders a chart value already in milliseconds with a compact unit.
func fmtMs(ms float64) string {
	switch {
	case ms >= 1000:
		return fmt.Sprintf("%.2fs", ms/1000)
	case ms >= 100:
		return fmt.Sprintf("%.0fms", ms)
	case ms >= 10:
		return fmt.Sprintf("%.1fms", ms)
	default:
		return fmt.Sprintf("%.1fms", ms)
	}
}

// fmtMiB renders a chart value already in MiB.
func fmtMiB(mib float64) string {
	if mib >= 1024 {
		return fmt.Sprintf("%.2fGiB", mib/1024)
	}
	return fmt.Sprintf("%.0fMiB", mib)
}

// fmtGridDur renders a grid duration in the natural PromQL unit.
func fmtGridDur(d time.Duration) string {
	switch {
	case d == 0:
		return "0s"
	case d%time.Hour == 0:
		return fmt.Sprintf("%dh", d/time.Hour)
	case d%time.Minute == 0:
		return fmt.Sprintf("%dm", d/time.Minute)
	case d%time.Second == 0:
		return fmt.Sprintf("%ds", d/time.Second)
	default:
		return d.String()
	}
}

// fmtBytes renders a byte count as a readable binary size.
func fmtBytes(n int64) string {
	const (
		kib = 1 << 10
		mib = 1 << 20
		gib = 1 << 30
	)
	switch {
	case n >= gib:
		return fmt.Sprintf("%.2f GiB", float64(n)/float64(gib))
	case n >= mib:
		return fmt.Sprintf("%d MiB", (n+mib/2)/mib)
	case n >= kib:
		return fmt.Sprintf("%d KiB", (n+kib/2)/kib)
	default:
		return fmt.Sprintf("%d B", n)
	}
}

// fmtInt renders an int with thousands separators.
func fmtInt(n int64) string {
	neg := n < 0
	if neg {
		n = -n
	}
	s := fmt.Sprintf("%d", n)
	var out []byte
	for i, c := range []byte(s) {
		if i > 0 && (len(s)-i)%3 == 0 {
			out = append(out, ',')
		}
		out = append(out, c)
	}
	if neg {
		return "-" + string(out)
	}
	return string(out)
}

// fmtFloatNs rounds ns/op to whole ns.
func fmtFloatNs(f float64) string {
	if f <= 0 {
		return "—"
	}
	return fmtInt(int64(f + 0.5))
}

// shortName truncates a long benchmark name.
func shortName(s string) string {
	const maxLen = 40
	if len(s) <= maxLen {
		return s
	}
	return s[:maxLen-1] + "…"
}
