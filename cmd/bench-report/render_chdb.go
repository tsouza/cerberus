type docInput struct {
	wins   []headlineWin
	curves []scalingCurve
	e2e    []e2eResult
	micro  []microResult

	iters     int
	benchtime string

	metricRows, logRows, traceRows int64

	goVersion string
	goarch    string
	numCPU    int
}

// align enumerates a markdown table column's alignment for the separator
// row and cell padding.
type align int

const (
	alignLeft align = iota
	alignRight
)

// writeTable emits a markdownlint-MD060-compliant aligned table: every
// pipe lines up because each cell is padded to its column's max width.
// aligns gives the per-column alignment (left/right); header + rows must
// all have len(aligns) cells.
func writeTable(b *strings.Builder, aligns []align, header []string, rows [][]string) {
	widths := make([]int, len(aligns))
	for i, h := range header {
		widths[i] = utf8.RuneCountInString(h)
	}
	for _, r := range rows {
		for i, c := range r {
			if w := utf8.RuneCountInString(c); w > widths[i] {
				widths[i] = w
			}
		}
	}
	// The separator cell is `---`/`---:`; ensure the column is wide enough
	// for it (min 3, +1 for the right-align colon).
	for i, a := range aligns {
		min := 3
		if a == alignRight {
			min = 4
		}
		if widths[i] < min {
			widths[i] = min
		}
	}

	writeRow := func(cells []string) {
		b.WriteString("|")
		for i, c := range cells {
			pad := widths[i] - utf8.RuneCountInString(c)
			if aligns[i] == alignRight {
				b.WriteString(" " + strings.Repeat(" ", pad) + c + " |")
			} else {
				b.WriteString(" " + c + strings.Repeat(" ", pad) + " |")
			}
		}
		b.WriteString("\n")
	}
	writeRow(header)
	// separator
	b.WriteString("|")
	for i, a := range aligns {
		if a == alignRight {
			b.WriteString(" " + strings.Repeat("-", widths[i]-1) + ": |")
		} else {
			b.WriteString(" " + strings.Repeat("-", widths[i]) + " |")
		}
	}
	b.WriteString("\n")
	for _, r := range rows {
		writeRow(r)
	}
}

// renderDoc assembles the full benchmarks.md. Timings are rounded to
// reduce single-process noise; ratios (the stable signal) carry one
// decimal. Tables are emitted via writeTable so pipes align (MD060).
func renderDoc(in docInput) string {
	var b strings.Builder

	b.WriteString("# Cerberus benchmarks\n\n")
	b.WriteString("Live performance measurements for the cerberus query gateway: optimizer ")
	b.WriteString("before/after wins, scaling curves, per-stage micro-benchmarks, and ")
	b.WriteString("end-to-end query latency on a realistic dataset. Every number here is ")
	b.WriteString("**measured**, not estimated — the document is regenerated by ")
	b.WriteString("[`just bench-report`](#methodology--how-to-regenerate).\n\n")

	renderMethodology(&b, in)
	renderHeadlines(&b, in)
	renderScaling(&b, in)
	renderMicro(&b, in)
	renderE2E(&b, in)

	b.WriteString("## See also\n\n")
	b.WriteString("- [`docs/performance.md`](performance.md) — the optimizer + perf ")
	b.WriteString("architecture these numbers exercise.\n")
	b.WriteString("- [`docs/test-strategy.md`](test-strategy.md) — the perf-assessment ")
	b.WriteString("test layers (scaling harness, corpus profiler, cardinality ratchet) ")
	b.WriteString("that gate structural regressions in CI.\n")

	return b.String()
}

func renderMethodology(b *strings.Builder, in docInput) {
	b.WriteString("## Methodology / how to regenerate\n\n")
	b.WriteString("```sh\njust bench-report\n```\n\n")
	b.WriteString("Regenerates this file in place. Requires `libchdb.so` (`just chdb-install`) — ")
	b.WriteString("all measurements run in-process against an embedded chDB ClickHouse engine, ")
	b.WriteString("and the optimized SQL shapes are produced by driving real queries through ")
	b.WriteString("the cerberus lowering pipeline (`internal/{promql,logql,traceql}` → ")
	b.WriteString("`internal/chsql`), so the measured SQL is the SQL cerberus emits.\n\n")

	b.WriteString("**Determinism.** Structural metrics — fan_factor, peak intermediate ")
	b.WriteString("cardinality, granules read, `allocs/op` — are deterministic and committed ")
	b.WriteString("verbatim. Timings — `ns/op`, wall-ms, e2e latency — are machine-dependent; ")
	b.WriteString("they are presented as **speedup ratios** wherever a before/after exists ")
	b.WriteString("(stable across hardware) and labelled *indicative* where absolute. This ")
	b.WriteString("document is a manually-regenerated artifact, **not** a CI gate: no test ")
	b.WriteString("fails on timing drift. Structural regressions are guarded separately by ")
	b.WriteString("the cardinality ratchet (`test/perf/cardinality_ratchet_test.go`) and the ")
	b.WriteString("scaling harness (`test/perf/scaling/`).\n\n")

	fmt.Fprintf(b, "**Sample count.** Each chDB wall timing is best-of-%d (the floor strips ", in.iters)
	fmt.Fprintf(b, "GC / scheduler jitter); micro-benchmarks use `-benchtime %s`.\n\n", in.benchtime)

	fmt.Fprintf(b, "**Reference hardware.** Indicative timings measured on `%s` / `%s` / ",
		in.goVersion, in.goarch)
	fmt.Fprintf(b, "%d logical CPUs. Treat absolute timings as order-of-magnitude; ratios are portable.\n\n",
		in.numCPU)
}

func renderHeadlines(b *strings.Builder, in docInput) {
	b.WriteString("## Headline before/after wins\n\n")
	b.WriteString("Each win pairs the **naive** pre-optimization SQL shape against the ")
	b.WriteString("**optimized** shape cerberus actually emits, both run live on chDB over a ")
	b.WriteString("seeded dataset. The `win` column is the headline ratio: a deterministic ")
	b.WriteString("structural ratio where one exists (granules read, rows scanned, peak ")
	b.WriteString("intermediate cardinality), else the indicative wall-time speedup. The ")
	b.WriteString("per-win detail below carries both axes.\n\n")

	var rows [][]string
	for _, w := range in.wins {
		ratio, basis := headlineRatio(w)
		rows = append(rows, []string{w.Name, fmt.Sprintf("%.1f×", ratio), basis})
	}
	writeTable(b, []align{alignLeft, alignRight, alignLeft},
		[]string{"optimization", "win", "basis"}, rows)
	b.WriteString("\n")

	// Per-win detail as a list (long descriptions go here, not in cells).
	for _, w := range in.wins {
		fmt.Fprintf(b, "### %s\n\n", w.Name)
		fmt.Fprintf(b, "- **Scenario:** %s\n", w.Param)
		fmt.Fprintf(b, "- **Naive:** %s\n", w.Naive)
		fmt.Fprintf(b, "- **Optimized:** %s\n", w.Optimized)
		if w.StructRatio > 0 {
			fmt.Fprintf(b, "- **%s (deterministic):** %s → %s (**%.1f× fewer**)\n",
				w.StructLabel, fmtInt(w.NaiveStruct), fmtInt(w.OptStruct), w.StructRatio)
		}
		if w.WallNote != "" {
			fmt.Fprintf(b, "- **Wall (indicative):** %s → %s — %s\n",
				roundDur(w.NaiveWall), roundDur(w.OptWall), w.WallNote)
		} else {
			fmt.Fprintf(b, "- **Wall (indicative):** %s → %s (**%.1f× faster**)\n",
				roundDur(w.NaiveWall), roundDur(w.OptWall), w.Speedup)
		}
		b.WriteString("\n")
	}
}

// headlineRatio picks the headline ratio + a short basis label for the
// summary table, honouring each win's Primary axis.
func headlineRatio(w headlineWin) (float64, string) {
	if w.Primary == "structural" && w.StructRatio > 0 {
		return w.StructRatio, w.StructLabel + " (deterministic)"
	}
	return w.Speedup, "wall time (indicative)"
}

func renderScaling(b *strings.Builder, in docInput) {
	b.WriteString("## Scaling curves\n\n")
	b.WriteString("Wall-time and `fan_factor` (peak intermediate cardinality ÷ scan rows) vs ")
	b.WriteString("the real fan-out multiplier for each registered construct ")
	b.WriteString("(`test/perf/scaling/`). A bounded shape keeps `fan_factor` ~flat as the ")
	b.WriteString("parameter grows — the cardinality stays a small constant multiple of the ")
	b.WriteString("scan, never `rows × parameter`.\n\n")

	for _, c := range in.curves {
		fmt.Fprintf(b, "### %s — sweep %s\n\n", c.Name, c.Param)
		fmt.Fprintf(b, "Scan rows held fixed at %s.\n\n", fmtInt(c.ScanRows))
		var rows [][]string
		for _, p := range c.Points {
			rows = append(rows, []string{
				fmt.Sprintf("%d", p.Param), roundDur(p.Wall),
				fmtInt(p.PeakRows), fmt.Sprintf("%.2f", p.FanRatio),
			})
		}
		writeTable(b, []align{alignRight, alignRight, alignRight, alignRight},
			[]string{"param", "wall", "peak rows", "fan_factor"}, rows)
		fmt.Fprintf(b, "\nParameter grew **%.1f×** across the sweep; wall grew **%.1f×** ",
			c.ParamGrowth, c.WallGrowth)
		if c.WallGrowth < c.ParamGrowth {
			b.WriteString("(sub-linear). ")
		} else {
			b.WriteString("(tracks the parameter — see the residual finding below). ")
		}
		fmt.Fprintf(b, "`fan_factor` stayed bounded (%.2f → %.2f).\n\n",
			c.Points[0].FanRatio, c.Points[len(c.Points)-1].FanRatio)
	}

	b.WriteString("> Note: the `setop_chain` wall is the documented residual super-linearity ")
	b.WriteString("(each `or` level re-scans the accumulated relation; the cardinality axis is ")
	b.WriteString("flat). It is quarantined as a tracked finding in the scaling harness ")
	b.WriteString("(N-ary single-pass flatten is the fix); the harness still hard-gates the ")
	b.WriteString("cardinality axis.\n\n")
}

func renderMicro(b *strings.Builder, in docInput) {
	b.WriteString("## Micro-benchmarks\n\n")
	b.WriteString("Per-stage Go benchmarks across the pipeline. `allocs/op` is deterministic; ")
	b.WriteString("`ns/op` is indicative.\n\n")

	var rows [][]string
	for _, m := range in.micro {
		rows = append(rows, []string{
			m.Pkg, shortName(m.Name), fmtFloatNs(m.NsPerOp),
			fmtInt(m.BytesOp), fmt.Sprintf("%d", m.AllocsOp),
		})
	}
	writeTable(b, []align{alignLeft, alignLeft, alignRight, alignRight, alignRight},
		[]string{"package", "benchmark", "ns/op", "B/op", "allocs/op"}, rows)
	b.WriteString("\n")
}

func renderE2E(b *strings.Builder, in docInput) {
	b.WriteString("## End-to-end query latency\n\n")
	fmt.Fprintf(b, "Representative queries lowered + emitted through the real cerberus ")
	b.WriteString("pipeline and executed on a large synthetic OTel dataset: ")
	fmt.Fprintf(b, "**%s** metric samples, **%s** log records, **%s** spans ",
		fmtInt(in.metricRows), fmtInt(in.logRows), fmtInt(in.traceRows))
	b.WriteString("(generated server-side via `numbers(N)`). Latency is best-of-")
	fmt.Fprintf(b, "%d (indicative).\n\n", in.iters)

	var rows [][]string
	for _, r := range in.e2e {
		rows = append(rows, []string{r.Name, r.Head, fmtInt(r.ScanRows), roundDur(r.Wall)})
	}
	writeTable(b, []align{alignLeft, alignLeft, alignRight, alignRight},
		[]string{"query", "head", "scan rows", "latency"}, rows)
	b.WriteString("\nQuery shapes:\n\n")
	for _, r := range in.e2e {
		fmt.Fprintf(b, "- **%s** (`%s`): `%s`\n", r.Name, r.Head, r.Query)
	}
	b.WriteString("\n")
}

// --- formatting helpers --------------------------------------------------

// roundDur rounds a duration to a stable, low-noise unit: µs under 1ms,
// ms under 1s, else 2-decimal seconds. Keeps regenerated diffs small.
func roundDur(d time.Duration) string {
	switch {
	case d < time.Microsecond:
		return d.String()
	case d < time.Millisecond:
		return fmt.Sprintf("%dµs", d.Round(time.Microsecond)/time.Microsecond)
	case d < time.Second:
		return fmt.Sprintf("%.1fms", float64(d)/float64(time.Millisecond))
	default:
		return fmt.Sprintf("%.2fs", d.Seconds())
	}
}

// fmtInt renders an int with thousands separators for readability.
func fmtInt(n int64) string {
	neg := n < 0
	if neg {
		n = -n
	}
	s := fmt.Sprintf("%d", n)
	var out []byte
	for i, c := range []byte(s) {
		if i > 0 && (len(s)-i)%3 == 0 {
			out = append(out, ',')
		}
		out = append(out, c)
	}
	if neg {
		return "-" + string(out)
	}
	return string(out)
}

// fmtFloatNs rounds ns/op to whole ns (sub-ns precision is noise).
func fmtFloatNs(f float64) string {
	if f <= 0 {
		return "—"
	}
	return fmtInt(int64(f + 0.5))
}

// shortName truncates a long benchmark name to keep MD060 table columns
// manageable.
func shortName(s string) string {
	const max = 40
	if len(s) <= max {
		return s
	}
	return s[:max-1] + "…"
}
