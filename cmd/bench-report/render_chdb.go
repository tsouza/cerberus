// align enumerates a markdown table column's alignment for the separator
// row and cell padding.
type align int

const (
	alignLeft align = iota
	alignRight
)

// writeTable emits a markdownlint-MD060-compliant aligned table: every
// pipe lines up because each cell is padded to its column's max width.
// aligns gives the per-column alignment (left/right); header + rows must
// all have len(aligns) cells.
func writeTable(b *strings.Builder, aligns []align, header []string, rows [][]string) {
	widths := make([]int, len(aligns))
	for i, h := range header {
		widths[i] = utf8.RuneCountInString(h)
	}
	for _, r := range rows {
		for i, c := range r {
			if w := utf8.RuneCountInString(c); w > widths[i] {
				widths[i] = w
			}
		}
	}
	// The separator cell is `---`/`---:`; ensure the column is wide enough
	// for it (min 3, +1 for the right-align colon).
	for i, a := range aligns {
		minW := 3
		if a == alignRight {
			minW = 4
		}
		if widths[i] < minW {
			widths[i] = minW
		}
	}

	writeRow := func(cells []string) {
		b.WriteString("|")
		for i, c := range cells {
			pad := widths[i] - utf8.RuneCountInString(c)
			if aligns[i] == alignRight {
				b.WriteString(" " + strings.Repeat(" ", pad) + c + " |")
			} else {
				b.WriteString(" " + c + strings.Repeat(" ", pad) + " |")
			}
		}
		b.WriteString("\n")
	}
	writeRow(header)
	// separator
	b.WriteString("|")
	for i, a := range aligns {
		if a == alignRight {
			b.WriteString(" " + strings.Repeat("-", widths[i]-1) + ": |")
		} else {
			b.WriteString(" " + strings.Repeat("-", widths[i]) + " |")
		}
	}
	b.WriteString("\n")
	for _, r := range rows {
		writeRow(r)
	}
}

// renderDoc assembles the full benchmarks.md, written to read top-to-bottom
// like a story: what we measure → where → how → the end-to-end numbers →
// the route × native-rate matrix → scaling → the per-component micro costs →
// how to reproduce. Charts are emitted as committed SVGs alongside the doc
// and referenced inline. Timings are rounded to keep regenerated diffs
// small; deterministic ratios carry one decimal.
func renderDoc(in docInput) (string, error) {
	var b strings.Builder

	renderIntro(&b, in)
	renderEnvironment(&b, in)
	renderMethodology(&b, in)
	if err := renderE2E(&b, in); err != nil {
		return "", err
	}
	if err := renderMatrix(&b, in); err != nil {
		return "", err
	}
	renderSharded(&b, in)
	if err := renderScaling(&b, in); err != nil {
		return "", err
	}
	renderHeadlines(&b, in)
	renderMicro(&b, in)
	renderReproducing(&b)
	renderSeeAlso(&b)

	return b.String(), nil
}

