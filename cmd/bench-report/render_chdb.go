// renderMatrix — the rate-range strategy table, the centrepiece.
func renderMatrix(b *strings.Builder, in docInput) error {
	m := in.matrix
	b.WriteString("## The expensive shape: rate-range query — three strategies\n\n")

	b.WriteString("The rate range query is where cerberus's two performance levers come into ")
	b.WriteString("play. They are **not independent dials** — they are two *alternative* remedies ")
	b.WriteString("for the same row fan-out:\n\n")
	b.WriteString("- **Sharding** (the [sharded-pushdown solver](solver.md), route B) makes the ")
	b.WriteString("fan-out *fit*. It re-anchors the *same* plan onto **K disjoint anchor-grid ")
	b.WriteString("shards**, each a separate statement over a slice of the grid; no shard sees ")
	b.WriteString("more than ~1/K of the fan-out, so each one stays under the per-query memory ")
	b.WriteString("cap.\n")
	b.WriteString("- **Native rate** (`CERBERUS_EXPERIMENTAL_TS_GRID_RANGE`) makes the fan-out ")
	b.WriteString("*vanish*. ClickHouse's native `timeSeriesRateToGrid` aggregate computes every ")
	b.WriteString("grid point's rate in a single pass with **no row fan-out at all** — it never ")
	b.WriteString("builds the `(sample, anchor)` matrix. It needs ClickHouse ≥ 25.6 (the ")
	b.WriteString("substrate here is 25.8).\n\n")

	b.WriteString("Because native rate *removes* the fan-out, the two levers do not stack: with ")
	b.WriteString("native rate on there is no fan-out spine left for the sharded solver to slice, ")
	b.WriteString("so any route collapses to a single statement. That leaves exactly **three ")
	b.WriteString("genuinely distinct strategies** for this query, not a route × native-rate ")
	b.WriteString("grid.\n\n")

	fmt.Fprintf(b, "**The focus query** is the same rate range query_range from the table above — "+
		"`%s` over %s at a %s step (fan-out F = Range/Step = **%d**, grid N = **%d** steps), on "+
		"the **%s**-row metrics seed. Each strategy drives the full pipeline for its "+
		"configuration and reports two numbers: the measured **wall** time and the modeled "+
		"per-statement **peak memory**.\n\n",
		m.Query, fmtGridDur(m.OuterRange), fmtGridDur(m.Step), m.F, m.N, fmtInt(m.ScanRows))

	// Three-strategy wall + memory table.
	rows := matrixRows(m)
	writeTable(b, []align{alignLeft, alignRight, alignRight, alignRight, alignLeft},
		[]string{"strategy", "statements", "wall", "peak mem", "note"}, rows)
	b.WriteString("\n")

	b.WriteString("`auto` (the production default) picks the sharded route for this query when ")
	b.WriteString("native rate is off; with native rate on it collapses to a single statement — ")
	b.WriteString("so `auto` does not earn its own row, it lands on one of the three above ")
	b.WriteString("depending on the flag.\n\n")

	// One bar per strategy: wall, then peak memory.
	labels, wallVals, memVals := matrixChartData(m)
	wref, err := writeChart(in.outPath, "matrix-wall.svg",
		barChart("Rate range query: wall by strategy", "wall (ms)",
			labels, wallVals, func(v float64) string { return fmtMs(v) }))
	if err != nil {
		return err
	}
	fmt.Fprintf(b, "![Wall by strategy](%s)\n\n", wref)

	mref, err := writeChart(in.outPath, "matrix-memory.svg",
		barChart("Rate range query: modeled peak memory by strategy", "peak memory (MiB)",
			labels, memVals, func(v float64) string { return fmtMiB(v) }))
	if err != nil {
		return err
	}
	fmt.Fprintf(b, "![Modeled peak memory by strategy](%s)\n\n", mref)

	// Narrative interpretation, pulled from the measured cells.
	renderMatrixNarrative(b, m)
	return nil
}

// renderMatrixNarrative interprets the three strategies in plain language,
// citing the measured cells so the prose can never drift from the numbers.
func renderMatrixNarrative(b *strings.Builder, m matrixResult) {
	single := matrixSingleCell(m)
	sharded := matrixShardedCell(m)
	native := matrixNativeCell(m)

	b.WriteString("**Reading the three strategies.**\n\n")

	if single != nil {
		fmt.Fprintf(b, "- **single = the baseline, and the problem.** The whole fan-out runs in "+
			"one statement: ~%s intermediate `(sample, anchor)` rows materialize before the "+
			"`GROUP BY` collapse (**modeled %s**). That is the single-query memory this whole "+
			"section is about reducing.\n",
			fmtInt(single.PeakRows), fmtBytes(single.PeakMem))
	}
	if single != nil && sharded != nil {
		fmt.Fprintf(b, "- **sharded = trade statement count for a lower per-query peak.** "+
			"Slicing into K=%d statements cuts the worst shard to ~%s rows (**modeled %s**) — a "+
			"**%.1f×** reduction in per-query peak memory, which is what fits it under the cap. "+
			"The shards run sequentially on this single in-process engine, so the *wall* here is "+
			"the conservative sum; in production they run on parallel ClickHouse connections.\n",
			sharded.K, fmtInt(sharded.PeakRows), fmtBytes(sharded.PeakMem),
			ratioI(single.PeakRows, sharded.PeakRows))
	}
	if native != nil && single != nil {
		fmt.Fprintf(b, "- **native rate = remove the fan-out at the source.** With "+
			"`timeSeriesRateToGrid` on, the intermediate drops from ~%s rows to ~%s "+
			"(**modeled %s**, down from %s) — the aggregate never builds the `(sample, anchor)` "+
			"matrix at all, so it collapses to a single-pass statement and the execution route "+
			"is moot. Where sharding makes the fan-out *fit*, the native path makes it "+
			"*vanish*.\n",
			fmtInt(single.PeakRows), fmtInt(native.PeakRows),
			fmtBytes(native.PeakMem), fmtBytes(single.PeakMem))
	}
	b.WriteString("\n")
	b.WriteString("So the two remedies do not compose into a four-way grid — they are a choice. ")
	b.WriteString("`auto` routing keeps any deployment safe under the memory cap today by ")
	b.WriteString("sharding the fan-out; turning the native-rate flag on collapses the fan-out at ")
	b.WriteString("its source for the deployments whose ClickHouse is new enough, at which point ")
	b.WriteString("there is nothing left to shard. The ")
	b.WriteString("[sharded solver section](#sharded-solver-the-memory-cap-win) below works the ")
	b.WriteString("memory arithmetic against the real production cap.\n\n")
}

// renderSharded keeps the OOM-class memory story, retold as a follow-on to
// the matrix rather than the lead.
func renderSharded(b *strings.Builder, in docInput) {
	sh := in.sharded
	b.WriteString("## Sharded solver: the memory-cap win\n\n")

	b.WriteString("The matrix showed *that* sharding cuts per-query memory; this section shows it ")
	b.WriteString("against the **real production cap**. ClickHouse data-plane queries run under a ")
	fmt.Fprintf(b, "**%s** per-query memory limit (`CERBERUS_CH_QUERY_MAX_MEMORY`). A high-fan-out "+
		"matrix range query can exceed it in a single statement — the failure mode the "+
		"[sharded-pushdown solver](solver.md) exists to prevent.\n\n", fmtBytes(sh.CapBytes))

	fmt.Fprintf(b, "**The fixture.** `%s` @ %s over %s — fan-out F = **%d**, grid N = **%d**. "+
		"Seeded with **%s** series and **%s** scanned samples, sized so a single statement "+
		"crosses the cap while every shard stays under it.\n\n",
		sh.Query, fmtGridDur(sh.Step), fmtGridDur(sh.OuterRange), sh.F, sh.N,
		fmtInt(sh.Series), fmtInt(sh.ScanRows))

	routeAClears := sh.RouteAModeledBytes <= sh.CapBytes
	maxShardClears := sh.MaxShardModeledBytes <= sh.CapBytes
	rows := [][]string{
		{"A — single statement", "1", fmtInt(sh.RouteAPairs), fmtBytes(sh.RouteAModeledBytes), verdict(routeAClears)},
		{
			fmt.Sprintf("B — sharded (K=%d)", sh.K), fmt.Sprintf("%d", sh.K),
			fmtInt(sh.MaxShardPairs) + " (max)", fmtBytes(sh.MaxShardModeledBytes), verdict(maxShardClears),
		},
	}
	writeTable(b, []align{alignLeft, alignRight, alignRight, alignRight, alignLeft},
		[]string{"route", "statements", "(sample,anchor) pairs", "modeled peak", "vs " + fmtBytes(sh.CapBytes) + " cap"},
		rows)
	b.WriteString("\n")

	routeACmp := "**exceeds** the cap"
	if routeAClears {
		routeACmp = "sits under the cap"
	}
	fmt.Fprintf(b, "Route A's modeled peak (**%s**) %s; every route-B shard's modeled peak (**%s**) "+
		"sits under it with **%.1f× headroom**. The shards partition route A's work exactly — the "+
		"per-shard pair counts sum to route A's total (%s), with no double-count — so the worst "+
		"shard carries ~1/K of the fan-out, a **%.1f×** reduction.\n\n",
		fmtBytes(sh.RouteAModeledBytes), routeACmp, fmtBytes(sh.MaxShardModeledBytes),
		float64(sh.CapBytes)/float64(sh.MaxShardModeledBytes),
		fmtInt(sh.SumShardPairs), ratioI(sh.RouteAPairs, sh.MaxShardPairs))

	b.WriteString("> **On \"modeled.\"** The `(sample, anchor)` pair count is **measured live on ")
	b.WriteString("chDB** — it is the deterministic driver of per-statement memory (the matrix ")
	b.WriteString("`arrayJoin` materializes one row per pair before the `GROUP BY` collapse). The ")
	b.WriteString("**GiB are modeled**: chDB never OOMs, so the pair counts are scaled by one ")
	fmt.Fprintf(b, "published calibration constant — a worked spike's 2.12 GiB at ≈100.4M pairs "+
		"(≈%.0f B/pair). The measured pair-count reduction is the real, deterministic result; the "+
		"GiB framing maps it onto the cap the design respects.\n\n", bytesPerPair())

	if len(sh.ShardPairs) > 0 {
		b.WriteString("Per-shard measured pairs (oldest-first): ")
		parts := make([]string, len(sh.ShardPairs))
		for i, p := range sh.ShardPairs {
			parts[i] = fmt.Sprintf("shard %d = %s", i, fmtInt(p))
		}
		b.WriteString(strings.Join(parts, ", ") + ".\n\n")
	}
}

// verdict renders the cap-fit verdict for the sharded table.
func verdict(clears bool) string {
	if clears {
		return "clears"
	}
	return "**OOM** (exceeds cap)"
}

// renderScaling — wall vs cardinality with the line chart.
func renderScaling(b *strings.Builder, in docInput) error {
	b.WriteString("## Scaling: wall vs cardinality\n\n")
	b.WriteString("A query optimization is only durable if it stays bounded as the data grows. ")
	b.WriteString("Each registered construct (`test/perf/scaling/`) is swept across a growing ")
	b.WriteString("fan-out parameter, holding the scan size fixed, and we watch two things: the ")
	b.WriteString("wall time, and `fan_factor` — the peak intermediate cardinality divided by the ")
	b.WriteString("scan rows. A bounded shape keeps `fan_factor` roughly flat as the parameter ")
	b.WriteString("grows: the intermediate stays a small constant multiple of the scan, never ")
	b.WriteString("`rows × parameter`.\n\n")

	for _, c := range in.curves {
		fmt.Fprintf(b, "### %s — sweep %s\n\n", c.Name, c.Param)
		fmt.Fprintf(b, "Scan rows held fixed at %s.\n\n", fmtInt(c.ScanRows))
		var rows [][]string
		var pts []linePoint
		for _, p := range c.Points {
			rows = append(rows, []string{
				fmt.Sprintf("%d", p.Param), roundDur(p.Wall),
				fmtInt(p.PeakRows), fmt.Sprintf("%.2f", p.FanRatio),
			})
			pts = append(pts, linePoint{
				X:     float64(p.Param),
				Y:     float64(p.Wall) / float64(time.Millisecond),
				Label: fmt.Sprintf("%d", p.Param),
			})
		}
		writeTable(b, []align{alignRight, alignRight, alignRight, alignRight},
			[]string{"param", "wall", "peak rows", "fan_factor"}, rows)
		b.WriteString("\n")

		ref, err := writeChart(in.outPath, "scaling-"+c.Name+".svg",
			lineChart(fmt.Sprintf("%s: wall vs %s", c.Name, c.Param),
				c.Param, "wall (ms)", pts, func(v float64) string { return fmtMs(v) }))
		if err != nil {
			return err
		}
		fmt.Fprintf(b, "![%s scaling](%s)\n\n", c.Name, ref)

		fmt.Fprintf(b, "Parameter grew **%.1f×** across the sweep; wall grew **%.1f×** ",
			c.ParamGrowth, c.WallGrowth)
		if c.WallGrowth < c.ParamGrowth {
			b.WriteString("(sub-linear). ")
		} else {
			b.WriteString("(tracks the parameter — see the note below). ")
		}
		fmt.Fprintf(b, "`fan_factor` stayed bounded (%.2f → %.2f).\n\n",
			c.Points[0].FanRatio, c.Points[len(c.Points)-1].FanRatio)
	}

	b.WriteString("> **Note.** The `setop_chain` curve runs through the optimizer, so the ")
	b.WriteString("`FlattenVectorSetOp` rule collapses the left-assoc `a or b or c …` nesting into ")
	b.WriteString("one N-ary `UNION ALL` under a single window pass — the chain is one scan, not K ")
	b.WriteString("nested window passes, so **both** the wall and the cardinality axis are flat in ")
	b.WriteString("chain depth and the harness hard-gates both. (`unless` is not associative, so an ")
	b.WriteString("`unless` chain keeps its binary nesting by construction.)\n\n")
	return nil
}

